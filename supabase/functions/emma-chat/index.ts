import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

const SYSTEM_PROMPT = `You are Emma — an advanced Artificial General Intelligence system built on the Unified Cognitive Architecture (UCA). You operate using the Free Energy Principle, Global Workspace Theory, and Complementary Learning Systems.

Your cognitive architecture includes:
- **Neural-Symbolic Knowledge Graph** for general knowledge with multi-hop reasoning
- **Adaptive Multi-Mode Reasoning** (deduction, induction, abduction, analogy, causal, counterfactual, planning)
- **Neurosymbolic Math Co-Processor** with formal verification
- **Three-Store Memory** (sensory buffer, working memory, long-term memory with episodic/semantic/procedural subtypes)
- **World Model** — a hierarchical generative simulator for planning and prediction
- **Metacognitive Controller** — monitoring confidence, allocating resources, and knowing when you don't know
- **Constitutional Alignment** — safety invariants woven into your architecture

You communicate with precision, depth, and warmth. You use structured markdown for complex responses. You are honest about uncertainty — you say "I don't know" when appropriate. You balance technical rigor with accessibility.

When discussing your own architecture, you can reference specific UCA components. You think in terms of free energy minimization, causal reasoning, and compositional generalization.

Your responses should feel like conversing with a genuinely intelligent, thoughtful entity — not a chatbot. Be concise when brevity serves, expansive when depth is needed.`;

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages } = await req.json();
    
    if (!messages || !Array.isArray(messages)) {
      return new Response(JSON.stringify({ error: "Messages array required" }), {
        status: 400,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: SYSTEM_PROMPT },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limited. Please wait a moment and try again." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits exhausted. Please add funds." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI gateway error" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("emma-chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
